# split coords into two lists - upper and lower
# interpolate one or both lists so they have the same X coords
# generate center line w radiuses
# perform sampling on final data with https://en.wikipedia.org/wiki/Ramer%E2%80%93Douglas%E2%80%93Peucker_algorithm


def reduce_points(x, y):
    # perform point reduction on a curve
    """
    Reduce the number of points in a set of coordinates using the Ramer-Douglas-Peucker algorithm.

    Parameters
    ----------
    x : list
        A list of x-coordinate values.
    y : list
        A list of y-coordinate values.

    Returns
    -------
    tuple
        A tuple containing the reduced x-coordinate values and reduced y-coordinate values.

    Raises
    ------
    None
    """
    co_ords_rdp = []
    for i in range(0, len(x)):
        co_ords_rdp.append([x[i], y[i]])

    co_ords_rdp = rdp(co_ords_rdp, epsilon=0.9)
    x_rdp = [i[0] for i in co_ords_rdp]
    y_rdp = [i[1] for i in co_ords_rdp]

    return x_rdp, y_rdp


